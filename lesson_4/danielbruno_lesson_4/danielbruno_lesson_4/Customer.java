package danielbruno_lesson_4;

public class Customer {
    private int id;
    private int type;
    private String addres;
    private String email;

    public int getId() {
        return this.id;
    }

    public int getType() {
        return this.type;
    }

    public String getAddres() {
        return this.addres;
    }

    public String getEmail() {
        return this.email;
    }
}
